DECLARE
NUMBER1 NUMBER:=7;
NUMBER2 NUMBER:=6;
REM NUMBER;

BEGIN
    REM:=MOD(NUMBER1, 2);
    IF REM=0 THEN
    DBMS_OUTPUT.PUT_LINE(NUMBER1 || ' IS EVEN');
    ELSE
    DBMS_OUTPUT.PUT_LINE(NUMBER1 || ' IS ODD');
    END IF;

    REM:=MOD(NUMBER2, 2);
    IF REM=0 THEN
    DBMS_OUTPUT.PUT_LINE(NUMBER2 || ' IS EVEN');
    ELSE
    DBMS_OUTPUT.PUT_LINE(NUMBER2 || ' IS ODD');
    END IF;
END;
